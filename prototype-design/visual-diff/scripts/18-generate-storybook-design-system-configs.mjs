#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = process.env.PYXIS_REPO_ROOT || path.resolve(scriptDir, '../../..');
const storybookUrl = process.env.PYXIS_COMPONENTS_STORYBOOK_URL || 'http://localhost:6006';
const storybookIndexPath = path.join(repoRoot, 'web/packages/pyxis-components/storybook-static/index.json');
const configRoot = path.join(repoRoot, 'prototype-design/visual-diff/storybook-components');
const catalogRoot = path.join(repoRoot, 'prototype-design/storybook-catalog');
const manifestPath = path.join(catalogRoot, 'manifest.json');

const props = [
  'display', 'position', 'box-sizing', 'width', 'height', 'min-width', 'min-height', 'max-width', 'max-height',
  'margin', 'padding', 'gap', 'font-family', 'font-size', 'font-weight', 'line-height', 'letter-spacing',
  'text-transform', 'text-align', 'color', 'background', 'background-color', 'border', 'border-color',
  'border-width', 'border-style', 'border-radius', 'box-shadow', 'opacity', 'align-items', 'justify-content',
  'grid-template-columns', 'flex-direction', 'white-space', 'overflow',
];

function ensureDir(dir) { fs.mkdirSync(dir, { recursive: true }); }
function q(v) { return JSON.stringify(v); }
function slugPart(v) { return String(v).toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, ''); }
function kebabComponentName(v) {
  return slugPart(String(v || '')
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1-$2'));
}
function scalar(value) {
  if (typeof value === 'string') return q(value);
  if (typeof value === 'number' || typeof value === 'boolean') return String(value);
  if (Array.isArray(value)) return `[${value.map(scalar).join(', ')}]`;
  if (value && typeof value === 'object') return `{ ${Object.entries(value).map(([k, v]) => `${k}: ${scalar(v)}`).join(', ')} }`;
  return 'null';
}
function groupOf(title) {
  const top = String(title || '').split('/')[0];
  if (top === 'Atoms') return 'atoms';
  if (top === 'Molecules') return 'molecules';
  if (top === 'Organisms') return 'organisms';
  if (top === 'Public') return 'public';
  return 'other';
}
function style(name, selector) {
  return [
    `  - name: ${q(name)}`,
    `    selector: ${q(selector)}`,
    `    include_bounds: true`,
    `    props: [${props.join(', ')}]`,
  ].join('\n');
}

function componentSlugFromTitle(title) {
  const raw = String(title || '').split('/').slice(1).join('-');
  const slug = kebabComponentName(raw);
  if (slug === 'atom-diff-fixture') return '';
  if (slug === 'icon') return 'icon';
  return slug;
}

function captureTargetSelector(entry) {
  const title = String(entry.title || '');
  const storyName = String(entry.name || '').toLowerCase();
  if (title === 'Atoms/Atom Diff Fixture') return '#storybook-root [data-pyxis-story-root], #storybook-root > *:first-child:not(:has([data-pyxis-story-root]))';
  if (/all|sizes|variants|statuses|colors|icons|icon-only|set|states|lineup/.test(storyName)) return '#storybook-root [data-pyxis-story-root], #storybook-root > *:first-child:not(:has([data-pyxis-story-root]))';
  if (title === 'Atoms/Icon' && storyName.replace(/\s+/g, '') === 'pyxislogo') return '#storybook-root [data-pyxis-component="pyxis-logo"][data-pyxis-part="root"]';
  if (title === 'Atoms/Icon' && storyName.replace(/\s+/g, '') === 'pyxismark') return '#storybook-root [data-pyxis-component="pyxis-mark"][data-pyxis-part="root"]';
  const component = componentSlugFromTitle(title);
  if (component) return `#storybook-root [data-pyxis-component="${component}"][data-pyxis-part="root"]`;
  return '#storybook-root > *:first-child';
}

function configFor(entry, relOut) {
  const captureSelector = captureTargetSelector(entry);
  const url = `${storybookUrl}/iframe.html?id=${encodeURIComponent(entry.id)}&viewMode=story`;
  const viewport = { width: 960, height: 720 };
  const prep = [
    `    type: script`,
    `    wait_for: ${q("document.querySelector('#storybook-root > *:first-child') && (!document.fonts || document.fonts.status === 'loaded')")}`,
    `    script: |`,
    `      document.body.style.margin = '0';`,
    `      document.documentElement.style.background = '#f3f1eb';`,
    `      const root = document.querySelector('#storybook-root');`,
    `      if (root) {`,
    `        root.style.minHeight = '0px';`,
    `      }`,
    `    after_wait_ms: 250`,
  ].join('\n');
  return [
    `# Generated by prototype-design/visual-diff/scripts/18-generate-storybook-design-system-configs.mjs.`,
    `# Purpose: Storybook implementation catalog extraction for Pyxis design-system stories.`,
    `metadata:`,
    `  slug: ${q(entry.id)}`,
    `  title: ${q(entry.title)}`,
    `  story_name: ${q(entry.name)}`,
    `  story_id: ${q(entry.id)}`,
    `  import_path: ${q(entry.importPath || '')}`,
    `  component_path: ${q(entry.componentPath || '')}`,
    `  source: ${q('web/packages/pyxis-components/storybook-static/index.json')}`,
    '',
    `original:`,
    `  name: ${q(entry.id)}`,
    `  url: ${q(url)}`,
    `  wait_ms: 0`,
    `  viewport: ${scalar(viewport)}`,
    `  root_selector: ${q('#storybook-root')}`,
    `  prepare:`,
    prep,
    '',
    `react:`,
    `  name: ${q(entry.id)}`,
    `  url: ${q(url)}`,
    `  wait_ms: 0`,
    `  viewport: ${scalar(viewport)}`,
    `  root_selector: ${q('#storybook-root')}`,
    `  prepare:`,
    prep,
    '',
    `styles:`,
    style('story-root', '#storybook-root'),
    style('capture-target', captureSelector),
    '',
    `output:`,
    `  dir: ${q(path.join(catalogRoot, 'artifacts', relOut))}`,
    `  write_json: true`,
    `  write_markdown: true`,
    `  write_pngs: true`,
    `  write_prepared_html: true`,
    `  write_inspect_json: true`,
    `  validate_pngs: true`,
    '',
    `modes: [capture]`,
    '',
  ].join('\n');
}

if (!fs.existsSync(storybookIndexPath)) {
  console.error(`Missing Storybook index: ${storybookIndexPath}`);
  process.exit(2);
}
ensureDir(configRoot);
ensureDir(catalogRoot);

const index = JSON.parse(fs.readFileSync(storybookIndexPath, 'utf8'));
const entries = Object.values(index.entries || index)
  .filter((entry) => entry.type === 'story')
  .filter((entry) => ['atoms', 'molecules', 'organisms', 'public'].includes(groupOf(entry.title)))
  .sort((a, b) => a.id.localeCompare(b.id));

const manifest = [];
for (const entry of entries) {
  const group = groupOf(entry.title);
  const component = kebabComponentName(entry.title.split('/').slice(1).join('-') || entry.title);
  const story = slugPart(entry.name);
  const relDir = path.join(group, component, story);
  const configPath = path.join(configRoot, relDir, 'config.css-visual-diff.yml');
  ensureDir(path.dirname(configPath));
  fs.writeFileSync(configPath, configFor(entry, relDir));
  manifest.push({
    storyId: entry.id,
    title: entry.title,
    storyName: entry.name,
    group,
    component,
    story,
    importPath: entry.importPath || '',
    componentPath: entry.componentPath || '',
    config: path.relative(repoRoot, configPath).split(path.sep).join('/'),
    output: path.relative(repoRoot, path.join(catalogRoot, 'artifacts', relDir)).split(path.sep).join('/'),
  });
}

fs.writeFileSync(manifestPath, JSON.stringify({
  generatedAt: new Date().toISOString(),
  sourceIndex: path.relative(repoRoot, storybookIndexPath).split(path.sep).join('/'),
  storybookUrl,
  configRoot: path.relative(repoRoot, configRoot).split(path.sep).join('/'),
  outputRoot: path.relative(repoRoot, path.join(catalogRoot, 'artifacts')).split(path.sep).join('/'),
  count: manifest.length,
  entries: manifest,
}, null, 2));

console.log(`Generated ${manifest.length} Storybook design-system configs under ${configRoot}`);
console.log(`Wrote manifest to ${manifestPath}`);
