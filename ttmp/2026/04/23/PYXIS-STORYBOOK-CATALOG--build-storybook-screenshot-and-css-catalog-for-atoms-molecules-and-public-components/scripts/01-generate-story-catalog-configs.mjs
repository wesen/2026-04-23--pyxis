#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const repoRoot = process.env.PYXIS_REPO_ROOT || '/home/manuel/code/wesen/2026-04-23--pyxis';
const ticketRoot = process.env.PYXIS_STORYBOOK_CATALOG_TICKET || path.join(
  repoRoot,
  'ttmp/2026/04/23/PYXIS-STORYBOOK-CATALOG--build-storybook-screenshot-and-css-catalog-for-atoms-molecules-and-public-components'
);
const storybookUrl = process.env.PYXIS_COMPONENTS_STORYBOOK_URL || 'http://localhost:6006';
const storybookStatic = process.env.PYXIS_COMPONENTS_STORYBOOK_STATIC || path.join(
  repoRoot,
  'web/packages/pyxis-components/storybook-static'
);
const indexPath = path.join(storybookStatic, 'index.json');
const configRoot = path.join(ticketRoot, 'sources/story-configs');
const catalogRoot = path.join(ticketRoot, 'various/story-catalog');

const topLevelAllow = new Set(['Atoms', 'Molecules', 'Organisms', 'Public']);
const rootSelector = '#storybook-root > *:first-child';
const componentSelector = [
  '#storybook-root > *:first-child [data-part]',
  '#storybook-root > *:first-child [data-comp]',
  '#storybook-root > *:first-child button',
  '#storybook-root > *:first-child input',
  '#storybook-root > *:first-child select',
  '#storybook-root > *:first-child textarea',
  '#storybook-root > *:first-child nav',
  '#storybook-root > *:first-child main',
  '#storybook-root > *:first-child section',
  '#storybook-root > *:first-child article',
  '#storybook-root > *:first-child form',
  '#storybook-root > *:first-child table',
  '#storybook-root > *:first-child h1',
  '#storybook-root > *:first-child h2',
  '#storybook-root > *:first-child h3',
  '#storybook-root > *:first-child h4',
  '#storybook-root > *:first-child h5',
  '#storybook-root > *:first-child img',
  '#storybook-root > *:first-child svg',
].join(', ');

const props = [
  'display',
  'position',
  'box-sizing',
  'width',
  'height',
  'min-width',
  'min-height',
  'max-width',
  'max-height',
  'margin',
  'padding',
  'gap',
  'font-family',
  'font-size',
  'font-weight',
  'line-height',
  'letter-spacing',
  'text-transform',
  'text-align',
  'color',
  'background',
  'background-color',
  'border',
  'border-color',
  'border-width',
  'border-style',
  'border-radius',
  'box-shadow',
  'opacity',
  'align-items',
  'justify-content',
  'grid-template-columns',
  'flex-direction',
  'white-space',
  'overflow',
];

function slugify(value) {
  return String(value)
    .trim()
    .toLowerCase()
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'story';
}

function yamlString(value) {
  return JSON.stringify(String(value));
}

function yamlList(values, indent = '      ') {
  return values.map((value) => `${indent}- ${value}`).join('\n');
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

if (!fs.existsSync(indexPath)) {
  console.error(`Storybook index.json not found: ${indexPath}`);
  console.error('Build component Storybook first: cd web && pnpm --filter pyxis-components build-storybook');
  process.exit(2);
}

const index = JSON.parse(fs.readFileSync(indexPath, 'utf8'));
const entries = Object.values(index.entries || {})
  .filter((entry) => entry.type === 'story')
  .filter((entry) => topLevelAllow.has(String(entry.title || '').split('/')[0]))
  .sort((a, b) => String(a.id).localeCompare(String(b.id)));

ensureDir(configRoot);
ensureDir(catalogRoot);

const manifest = [];
for (const entry of entries) {
  const [top = 'Unknown', component = 'Unknown'] = String(entry.title || 'Unknown/Unknown').split('/');
  const topSlug = slugify(top);
  const componentSlug = slugify(component);
  const storySlug = slugify(entry.name || entry.id);
  const slug = `${topSlug}-${componentSlug}-${storySlug}`;
  const relDir = `${topSlug}/${componentSlug}/${storySlug}`;
  const configDir = path.join(configRoot, topSlug, componentSlug);
  const configPath = path.join(configDir, `${storySlug}.css-visual-diff.yml`);
  const storyUrl = `${storybookUrl}/iframe.html?id=${encodeURIComponent(entry.id)}&viewMode=story`;
  const outDir = path.join(catalogRoot, 'artifacts', relDir);
  ensureDir(configDir);

  const content = `# Generated by scripts/01-generate-story-catalog-configs.mjs.
# Purpose: single-side Storybook extraction of screenshot, computed CSS, prepared HTML,
# and inspect JSON for the Pyxis visual catalog.
metadata:
  slug: ${yamlString(slug)}
  title: ${yamlString(`${entry.title} / ${entry.name}`)}
  story_id: ${yamlString(entry.id)}
  story_title: ${yamlString(entry.title)}
  story_name: ${yamlString(entry.name)}
  catalog_rel_dir: ${yamlString(relDir)}

original:
  name: storybook
  url: ${yamlString(storyUrl)}
  wait_ms: 0
  viewport: { width: 1200, height: 900 }
  root_selector: ${yamlString(rootSelector)}
  prepare:
    type: script
    wait_for: ${yamlString("document.querySelector('#storybook-root > *:first-child') && (!document.fonts || document.fonts.status === 'loaded')")}
    script: |
      const storyFrame = document.querySelector('#storybook-root > *:first-child');
      if (storyFrame) {
        storyFrame.style.minHeight = '0px';
        storyFrame.style.height = 'auto';
      }
      document.body.style.margin = '0';
    after_wait_ms: 150

react:
  name: storybook
  url: ${yamlString(storyUrl)}
  wait_ms: 0
  viewport: { width: 1200, height: 900 }
  root_selector: ${yamlString(rootSelector)}
  prepare:
    type: script
    wait_for: ${yamlString("document.querySelector('#storybook-root > *:first-child') && (!document.fonts || document.fonts.status === 'loaded')")}
    script: |
      const storyFrame = document.querySelector('#storybook-root > *:first-child');
      if (storyFrame) {
        storyFrame.style.minHeight = '0px';
        storyFrame.style.height = 'auto';
      }
      document.body.style.margin = '0';
    after_wait_ms: 150

sections:
  - name: story
    selector: ${yamlString(rootSelector)}

styles:
  - name: story-root
    selector: ${yamlString(rootSelector)}
    include_bounds: true
    props:
${yamlList(props)}
  - name: component-focus
    selector: ${yamlString(componentSelector)}
    include_bounds: true
    props:
${yamlList(props)}

output:
  dir: ${yamlString(outDir)}
  write_json: true
  write_markdown: true
  write_pngs: true
  write_prepared_html: true
  write_inspect_json: true
  validate_pngs: true

modes: [capture, cssdiff, pixeldiff, html-report]
`;
  fs.writeFileSync(configPath, content);
  manifest.push({
    id: entry.id,
    title: entry.title,
    name: entry.name,
    top,
    component,
    topSlug,
    componentSlug,
    storySlug,
    slug,
    relDir,
    url: storyUrl,
    config: path.relative(ticketRoot, configPath),
    output: path.relative(ticketRoot, outDir),
  });
}

fs.writeFileSync(path.join(catalogRoot, 'manifest.json'), JSON.stringify({
  generatedAt: new Date().toISOString(),
  storybookUrl,
  storybookStatic,
  configRoot: path.relative(ticketRoot, configRoot),
  artifactRoot: 'various/story-catalog/artifacts',
  count: manifest.length,
  stories: manifest,
}, null, 2));

console.log(`Generated ${manifest.length} configs under ${configRoot}`);
console.log(`Wrote manifest to ${path.join(catalogRoot, 'manifest.json')}`);
