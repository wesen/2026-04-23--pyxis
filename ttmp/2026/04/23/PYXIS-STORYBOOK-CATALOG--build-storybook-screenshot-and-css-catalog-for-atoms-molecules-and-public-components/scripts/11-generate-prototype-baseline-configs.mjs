function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function q(value) {
  return JSON.stringify(value);
}

function scalar(value) {
  if (typeof value === 'string') return q(value);
  if (typeof value === 'number' || typeof value === 'boolean') return String(value);
  if (value === null) return 'null';
  if (Array.isArray(value)) return `[${value.map(scalar).join(', ')}]`;
  if (typeof value === 'object') {
    return `{ ${Object.entries(value).map(([k, v]) => `${k}: ${scalar(v)}`).join(', ')} }`;
  }
  return q(String(value));
}

function styleLine({ name, selector, props }) {
  return [
    `  - name: ${q(name)}`,
    `    selector: ${q(selector)}`,
    `    include_bounds: true`,
    `    props: [${props.join(', ')}]`,
  ].join('\n');
}

function styleBlock(styles) {
  return styles.map((style) => styleLine(style)).join('\n');
}

function captureOutput(dir) {
  return [
    `output:`,
    `  dir: ${q(dir)}`,
    `  write_json: true`,
    `  write_markdown: true`,
    `  write_pngs: true`,
    `  write_prepared_html: true`,
    `  write_inspect_json: true`,
    `  validate_pngs: true`,
    '',
    `modes: [capture]`,
  ].join('\n');
}

function scriptPrepare(waitFor, extraLines = []) {
  const lines = [
    `      const root = document.querySelector('#root');`,
    `      if (root) {`,
    `        root.style.minHeight = '0px';`,
    `        root.style.height = 'auto';`,
    `      }`,
    `      document.body.style.margin = '0';`,
    ...extraLines.map((line) => `      ${line}`),
  ];
  return [
    `    type: script`,
    `    wait_for: ${q(waitFor)}`,
    `    script: |`,
    ...lines,
    `    after_wait_ms: 250`,
  ].join('\n');
}

function directGlobalPrepare({ waitFor, component, props, width, minHeight, background }) {
  return [
    `    type: direct-react-global`,
    `    wait_for: ${q(waitFor)}`,
    `    component: ${component}`,
    `    root_selector: ${q('#capture-root')}`,
    `    props: ${scalar(props)}`,
    `    width: ${width}`,
    `    min_height: ${minHeight}`,
    `    background: ${q(background)}`,
    `    after_wait_ms: 500`,
  ].join('\n');
}

