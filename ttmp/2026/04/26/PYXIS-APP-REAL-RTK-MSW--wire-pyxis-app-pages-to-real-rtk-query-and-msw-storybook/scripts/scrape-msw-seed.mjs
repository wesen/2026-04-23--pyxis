#!/usr/bin/env node
/*
 * Scrape a seeded Pyxis backend and capture protobuf-shaped JSON responses
 * for MSW fixtures.
 *
 * Example:
 *   node scripts/scrape-msw-seed.mjs \
 *     --base-url http://localhost:8080 \
 *     --cookie 'session=test-session-abc' \
 *     --out ../sources/msw-seed.json
 */

import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';

const args = parseArgs(process.argv.slice(2));
const baseUrl = (args['base-url'] ?? 'http://localhost:8080').replace(/\/$/, '');
const out = resolve(args.out ?? 'sources/msw-seed.json');
const cookie = args.cookie ?? process.env.PYXIS_SESSION_COOKIE ?? '';
const writeTs = args.ts ? resolve(args.ts) : undefined;

const endpoints = [
  { key: 'publicShows', path: '/api/public/shows', auth: false },
  { key: 'publicArchive', path: '/api/public/archive', auth: false },
  { key: 'publicArchiveStats', path: '/api/public/archive/stats', auth: false },
  { key: 'session', path: '/api/app/session', auth: true },
  { key: 'appShows', path: '/api/app/shows', auth: true },
  { key: 'bookings', path: '/api/app/bookings', auth: true },
  { key: 'artists', path: '/api/app/artists', auth: true },
  { key: 'calendar', path: '/api/app/calendar', auth: true },
  { key: 'attendance', path: '/api/app/attendance', auth: true },
  { key: 'auditLog', path: '/api/app/audit-log', auth: true },
  { key: 'settings', path: '/api/app/settings', auth: true },
];

const result = {
  metadata: {
    generatedAt: new Date().toISOString(),
    baseUrl,
    note: 'Generated by scrape-msw-seed.mjs from a seeded Pyxis backend. Review/redact before committing if the server contains private data.',
  },
  responses: {},
};

for (const endpoint of endpoints) {
  const headers = { accept: 'application/json' };
  if (endpoint.auth && cookie) headers.cookie = cookie;

  const url = `${baseUrl}${endpoint.path}`;
  process.stderr.write(`GET ${url}\n`);

  try {
    const response = await fetch(url, { headers });
    const text = await response.text();
    let body;
    try {
      body = text ? JSON.parse(text) : null;
    } catch {
      body = text;
    }

    result.responses[endpoint.key] = {
      path: endpoint.path,
      status: response.status,
      ok: response.ok,
      body,
    };
  } catch (error) {
    result.responses[endpoint.key] = {
      path: endpoint.path,
      status: 0,
      ok: false,
      error: error instanceof Error ? error.message : String(error),
    };
  }
}

await mkdir(dirname(out), { recursive: true });
await writeFile(out, `${JSON.stringify(result, null, 2)}\n`, 'utf8');
process.stderr.write(`Wrote ${out}\n`);

if (writeTs) {
  await mkdir(dirname(writeTs), { recursive: true });
  await writeFile(
    writeTs,
    `// Generated by scrape-msw-seed.mjs. Review before committing.\nexport const mswSeed = ${JSON.stringify(result, null, 2)} as const;\n`,
    'utf8',
  );
  process.stderr.write(`Wrote ${writeTs}\n`);
}

function parseArgs(argv) {
  const parsed = {};
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (!arg.startsWith('--')) continue;
    const key = arg.slice(2);
    const next = argv[i + 1];
    if (!next || next.startsWith('--')) {
      parsed[key] = true;
    } else {
      parsed[key] = next;
      i += 1;
    }
  }
  return parsed;
}
