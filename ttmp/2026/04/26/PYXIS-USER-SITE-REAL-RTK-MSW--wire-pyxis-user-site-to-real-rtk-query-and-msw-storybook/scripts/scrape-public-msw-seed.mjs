#!/usr/bin/env node
/*
 * Scrape unauthenticated Pyxis public API endpoints from a seeded Go backend
 * and save protobuf-shaped JSON responses for MSW fixture review.
 *
 * Example:
 *   node scripts/scrape-public-msw-seed.mjs \
 *     --base-url http://localhost:8080 \
 *     --out sources/public-msw-seed.json \
 *     --details 3
 */

import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';

const args = parseArgs(process.argv.slice(2));
const baseUrl = (args['base-url'] ?? 'http://localhost:8080').replace(/\/$/, '');
const out = resolve(args.out ?? 'sources/public-msw-seed.json');
const details = Number(args.details ?? 3);
const writeTs = args.ts ? resolve(args.ts) : undefined;

const result = {
  metadata: {
    generatedAt: new Date().toISOString(),
    baseUrl,
    details,
    note: 'Generated from unauthenticated public Pyxis API endpoints. Responses should already be protobuf JSON shapes from protojson.Marshal.',
  },
  responses: {},
};

result.responses.shows = await getJson('/api/public/shows');
result.responses.archive = await getJson('/api/public/archive');
result.responses.archiveStats = await getJson('/api/public/archive/stats');

const showIds = Array.isArray(result.responses.shows.body?.shows)
  ? result.responses.shows.body.shows.slice(0, details).map((show) => show.id).filter((id) => Number.isFinite(id))
  : [];

result.responses.showDetails = [];
for (const id of showIds) {
  result.responses.showDetails.push({ id, ...(await getJson(`/api/public/shows/${id}`)) });
}

await mkdir(dirname(out), { recursive: true });
await writeFile(out, `${JSON.stringify(result, null, 2)}\n`, 'utf8');
process.stderr.write(`Wrote ${out}\n`);

if (writeTs) {
  await mkdir(dirname(writeTs), { recursive: true });
  await writeFile(
    writeTs,
    `// Generated by scrape-public-msw-seed.mjs. Review before committing.\nexport const publicMswSeed = ${JSON.stringify(result, null, 2)} as const;\n`,
    'utf8',
  );
  process.stderr.write(`Wrote ${writeTs}\n`);
}

async function getJson(path) {
  const url = `${baseUrl}${path}`;
  process.stderr.write(`GET ${url}\n`);
  try {
    const response = await fetch(url, { headers: { accept: 'application/json' } });
    const text = await response.text();
    let body;
    try {
      body = text ? JSON.parse(text) : null;
    } catch {
      body = text;
    }
    return { path, status: response.status, ok: response.ok, body };
  } catch (error) {
    return {
      path,
      status: 0,
      ok: false,
      error: error instanceof Error ? error.message : String(error),
    };
  }
}

function parseArgs(argv) {
  const parsed = {};
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (!arg.startsWith('--')) continue;
    const key = arg.slice(2);
    const next = argv[i + 1];
    if (!next || next.startsWith('--')) {
      parsed[key] = true;
    } else {
      parsed[key] = next;
      i += 1;
    }
  }
  return parsed;
}
